RAW_EXACT_STANDS.trim().split('\n').forEach(line => {
  const parts = line.trim().split(';');
  if (parts.length >= 4) {
    const name = parts[0];
    const airport = parts[1];
    
    // Tự động phân tích format N010.48.36.000 (Độ.Phút.Giây.MiliGiây)
    const latMatch = parts[2].match(/[NS](\d+)\.(\d+)\.(\d+\.\d+)/);
    const lonMatch = parts[3].match(/[EW](\d+)\.(\d+)\.(\d+\.\d+)/);
    
    if (latMatch && lonMatch) {
      // Công thức: Độ + Phút/60 + Giây/3600
      let lat = parseInt(latMatch[1]) + (parseInt(latMatch[2])/60) + (parseFloat(latMatch[3])/3600);
      let lon = parseInt(lonMatch[1]) + (parseInt(lonMatch[2])/60) + (parseFloat(lonMatch[3])/3600);
      
      // Nếu là hướng South (Nam) hoặc West (Tây) thì tọa độ phải mang số âm
      if (parts[2].startsWith('S')) lat = -lat;
      if (parts[3].startsWith('W')) lon = -lon;

      AIRPORT_STANDS.push({ airport, name, lat, lon });
    }
  }
});


// ==================== CÔNG THỨC VÀ LOGIC TÍNH TÓAN ====================
// Công thức tính khoảng cách 2 điểm GPS (trả ra Mét)
function getDistanceInMeters(lat1, lon1, lat2, lon2) {
  const R = 6371e3; 
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLon = (lon2 - lon1) * Math.PI / 180;
  const a = Math.sin(dLat/2) * Math.sin(dLat/2) +
            Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
            Math.sin(dLon/2) * Math.sin(dLon/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return R * c; 
}

// Định vị phi công đang ở Stand nào
function findClosestStand(lat, lon) {
  let closestStand = null;
  // Bán kính 80 mét là lý tưởng nhất: bao đủ trọn thân máy bay và bù trừ được độ lệch do xài Scenery khác nhau
  let minDistance = 80; 
  
  for (const stand of AIRPORT_STANDS) {
    const dist = getDistanceInMeters(lat, lon, stand.lat, stand.lon);
    if (dist < minDistance) {
      minDistance = dist;
      closestStand = `${stand.airport} - ${stand.name}`;
    }
  }
  return closestStand;
}

// ==================== LẤY DỮ LIỆU VATSIM ====================
parentPort.on('message', async (msg) => {
  if (msg === 'update') {
    try {
      const res = await axios.get('https://data.vatsim.net/v3/vatsim-data.json', { timeout: 15000 });
      const data = res.data || {};
      const prefix = ['VV', 'VL', 'VD'];
      
      const controllers = (data.controllers || []).filter(c => prefix.includes(((c.callsign||'').slice(0,2) || '').toUpperCase()));
      
      let pilots = (data.pilots || []).filter(p => p.flight_plan && (prefix.includes(((p.flight_plan.departure||'').slice(0,2)||'').toUpperCase()) || prefix.includes(((p.flight_plan.arrival||'').slice(0,2)||'').toUpperCase())));
      
      // Kẹp biến stand vào phi công nếu đang nằm sát cổng
      pilots = pilots.map(p => {
         const gs = p.groundspeed || 0;
         if (gs < 5) {
             const stand = findClosestStand(p.latitude, p.longitude);
             if (stand) p.current_stand = stand;
         }
         return p;
      });

      parentPort.postMessage({ controllers, pilots });
    } catch (err) {
      parentPort.postMessage({ error: err.message || String(err) });
    }
  }
});